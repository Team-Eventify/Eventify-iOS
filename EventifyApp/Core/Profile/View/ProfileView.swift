	}

	// MARK: - Body

	var body: some View {
		NavigationStack {
			VStack {
				header
				List {
					Section {
						NavigationLink {
							AddEventView()
						} label: {
                            Text("action_add_event")
						}
					}

					Section {
						NavigationLink(destination: ConsoleView()) {
							Text("Pulse –ö–æ–Ω—Å–æ–ª—å")
						}
						
						NavigationLink {
							TestView()
