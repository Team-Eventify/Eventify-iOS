	/// - Parameter viewModel: –º–æ–¥–µ–ª—å —ç–∫—Ä–∞–Ω–∞ –ø—Ä–æ—Ñ–∏–ª—è
    init(viewModel: ProfileViewModel? = nil) {
		_viewModel = StateObject(
            wrappedValue: viewModel ?? ProfileViewModel(userService: UserService())
